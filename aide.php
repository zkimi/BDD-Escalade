      Voici les questions posées les plus fréquemments, si vous ne trouvez pas votre réponse, créez un compte et <a href="requests.php">créez une demande</a>, un administrateur vous répondra.

      <br>
      <h3>Comment créer un compte ?</h3>
      Rendez-vous dans la rubrique "<a href="register.php">Inscription</a>" puis remplissez tout les champs et enfin cliquez sur "<b>Inscription</b>". Si vous avez déjà un compte, rendez-vous sur la page "<a href="login.php">Connexion</a>".
      <hr>
      <h3>Comment créer un club ?</h3>
      Si vous êtes responsables d'un club d'escalade, créez-vous un compte sur notre site puis rendez vous dans la rubrique "<a href="requests.php">Mes demandes</a>". Enfin, créez une demande, remplissez le formulaire et spécifiez le motif comme "<b>Création d'un club</b>". Un administrateur analysera votre demande et sera susceptible de vous demander des informations professionnelles (mais <b>jamais</b> personnelles !).
      <hr>
      <h3>Comment devenir guide ?</h3>
      Si vous êtes un guide agréé et que votre club est inscrit sur ce site, il suffit d'en faire la demande auprès de <b>votre responsable</b> (le responsable du club inscrit). Seul lui pourra vous promouvoir en tant que "Guide". Vous devez aussi savoir que si vous êtes promu "Guide", vous devrez renseigner votre niveau d'encadrement maximal (qui doit <u>obligatoirement</u> être inférieur au vôtre).
      <hr>
      <h3>Comment analyser un parcours proposé ?</h3>
      Vous êtes membre et vous ne comprenez pas tout ce qui est affiché sur la page de votre club ? Pas de panique. Sur la page de votre club sont inscrits ses détails et dans la deuxième partie, ses parcours proposés (s'il y en a). Concernant ses parcours, vous aurez à disposition leur longueur/hauteur, leur titre, une description ainsi qu'un niveau minimal/recommandé qui sera spécifié dans le rond se trouvant à droite de ce dernier.
